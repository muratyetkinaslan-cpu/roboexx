def _seq(pairs, q):
    """('NOTA', çarpan) çiftlerini (frekans, ms) listesine çevirir.
    q = dörtlük nota süresi (ms)."""
    out = []
    for note, mult in pairs:
        out.append((_N.get(note, 0), int(q * mult)))
    return out


