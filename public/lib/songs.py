_song_pwm = {}


def play_song(pin, name):
    """Seçilen şarkıyı buzzer'da çalar.
    pin: buzzer pini, name: SONGS sözlüğündeki anahtar."""
    if name not in SONGS:
        return
    pairs, quarter = SONGS[name]
    if pin not in _song_pwm:
        _song_pwm[pin] = PWM(Pin(pin))
    p = _song_pwm[pin]
    for note, mult in pairs:
        freq = _N.get(note, 0)
        dur = int(quarter * mult)
        if freq > 0:
            p.freq(freq)
            p.duty_u16(20000)
        else:
            p.duty_u16(0)
        # Notanın %85'i ses, %15'i kısa boşluk — notalar ayrışsın
        time.sleep_ms(int(dur * 0.85))
        p.duty_u16(0)
        time.sleep_ms(int(dur * 0.15))
    p.duty_u16(0)
